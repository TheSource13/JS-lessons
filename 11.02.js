var multi = [
    [3,5,5,5],
    [3,5,5,5],
    [3,5,5,5],
    [5,5,5,5],
   ];

var sum = 0;
for(var k= 0; k< multi.length ; k){
   for (var i= 0; i< multi[k].length; i++){
       if( k === i)
       sum =sum + multi[k][i]; 
      
           
   }
   
}

console.log(sum); 


